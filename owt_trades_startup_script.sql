CREATE TABLE OpenTrades
(
ID INT NOT NULL AUTO_INCREMENT,
OpenDate DATE NOT NULL,
OpenDescription VARCHAR(50) NOT NULL,
Ticker VARCHAR(10) NOT NULL,
CreditDebit VARCHAR(20) NOT NULL,
TradeType VARCHAR(20) NOT NULL,
SetupType VARCHAR(20) NOT NULL,
Rating DECIMAL(13,4) NOT NULL,
Rec VARCHAR(20) NOT NULL,
OpenTradePrice DECIMAL(13,4) NOT NULL,
OpenPrice DECIMAL(13,4) NOT NULL,
InCom DECIMAL(13,4) NOT NULL,
PRIMARY KEY (ID)
);

CREATE TABLE ClosedTrades
(
ID INT NOT NULL AUTO_INCREMENT,
CloseDate DATE NOT NULL,
CloseTradePrice DECIMAL(13,4) NOT NULL,
ClosePrice DECIMAL(13,4) NOT NULL,
OutCom DECIMAL(13,4) NOT NULL,
CloseDescription VARCHAR(50) NOT NULL,
TradeID INT NOT NULL,
ExitReason VARCHAR(50),
CONSTRAINT FK_OpenTrades_ClosedTrades FOREIGN KEY (TradeID)
REFERENCES OpenTrades(ID),
PRIMARY KEY (ID)
);

CREATE TABLE Withdraws
(
ID INT NOT NULL AUTO_INCREMENT,
DrawDate DATE NOT NULL,
GrossAmount DECIMAL(13,4) NOT NULL,
WithheldAmount DECIMAL(13,4) NOT NULL,
NetAmount DECIMAL(13,4) NOT NULL,
Account VARCHAR(50) NOT NULL,
Category VARCHAR(50) NOT NULL,
Bank VARCHAR(50) NOT NULL,
PRIMARY KEY (ID)
);

CREATE TABLE Futures
(
ID INT NOT NULL AUTO_INCREMENT,
OpenCloseDate DATE NOT NULL,
TransactionTime TIME NOT NULL,
OrderId INT NOT NULL,
TradeDescription VARCHAR(50) NOT NULL,
